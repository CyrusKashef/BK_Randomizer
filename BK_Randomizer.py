def skip_ttc_grublin(mm, index):
    obj_id1 = mm[index]
    obj_id2 = mm[index + 1]
    if((obj_id1 == 0) and (obj_id2 == 6)):
        x_loc1 = mm[index - 8]
        x_loc2 = mm[index - 7]
        y_loc1 = mm[index - 6]
        y_loc2 = mm[index - 5]
        z_loc1 = mm[index - 4]
        z_loc2 = mm[index - 3]
        # TTC Grublin
        if((x_loc1 == 4) and (x_loc2 == 238) and #04EE
           (y_loc1 == 20) and (y_loc2 == 97) and #1461
           (z_loc1 == 241) and (z_loc2 == 246)): #F1F6
            print("Editing TTC OoB Blue Egg")
            return False
    return True

